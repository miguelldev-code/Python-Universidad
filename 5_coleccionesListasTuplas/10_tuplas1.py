# Acceder a los numeros de una tupla

# Crear una tupla de números
tupla_numeros = (10, 20, 30, 40, 50)

# Acceder al primer y último elemento
print("Primer elemento:", tupla_numeros[0])
print("Último elemento:", tupla_numeros[-1])

# Imprimir todos los elementos con un bucle for
for numero in tupla_numeros:
    print(numero)
