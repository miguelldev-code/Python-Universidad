# Empaquetado y desempaquetado de tuplas

# Crear una tupla con tres valores
tupla = (100, 200, 300)

# Desempaquetar la tupla en tres variables
a, b, c = tupla
print("Valores desempaquetados:", a, b, c)

# Intercambiar los valores de 'a' y 'b' usando desempaquetado
a, b = b, a
print("Valores intercambiados:", a, b)

