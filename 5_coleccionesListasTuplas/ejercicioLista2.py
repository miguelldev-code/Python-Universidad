# Suma de los elementos de una lista
# Calcula la suma de los elementos de una lista de números.


# Lista de números
numeros = [3, 7, 12, 5, 9]

# Variable para almacenar la suma
suma_total = 0

# Recorrer la lista y sumar cada elemento
for numero in numeros:
    suma_total += numero

print(f"La suma de los números es: {suma_total}")
